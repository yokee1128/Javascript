// 조건 - if/else-if/else
// 조건 - switch

const season = "spring";

switch (season) {
  case "spring":
    console.log("봄이네요");
    break;
  case "summer":
    console.log("여름이네요");
    break;
  case "fall":
    console.log("가을이네요");
    break;
  case "winter":
    console.log("겨울이네요");
    break;
  default:
    console.log("알수없네요");
    break;
}
